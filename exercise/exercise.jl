begin
	@model function bayesian_logistic_reg(X, y; v₀=10^2, V₀ = 10^2)
		# priors
		β0 ~ Normal(0, sqrt(v₀))
		nfeatures = size(X)[2]
		β ~ MvNormal(zeros(nfeatures), sqrt(V₀))
		# Likelihood
		μs = β0 .+ X * β
		# logistic transformations
		σs = logistic.(μs)
		for i in eachindex(y)
			y[i] ~ Bernoulli(σs[i])
		end
		return (; σs)
	end
end;
